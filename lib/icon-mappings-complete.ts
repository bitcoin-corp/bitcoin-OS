export const getCompleteThemedIcon = (iconId: string, theme: string = 'lucide') => {
  const mapping = completeIconMappings[iconId]
  if (!mapping) {
    // Fallback to default settings icon
    const defaultMapping = completeIconMappings['settings']
    return defaultMapping[theme as keyof CompleteIconMapping] || defaultMapping.lucide
  }
  
  return mapping[theme as keyof CompleteIconMapping] || mapping.lucide
}